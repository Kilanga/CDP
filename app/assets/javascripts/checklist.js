const LS_KEY='cdp_checked_v1';
const TL={action:'Action',verif:'Vérif.',doc:'Doc.',comm:'Comm.',risque:'Risque'};
const TC={action:'t-action',verif:'t-verif',doc:'t-doc',comm:'t-comm',risque:'t-risque'};
let curPhase=null,curTag=null;

// localStorage persistence
function load(){try{return new Set(JSON.parse(localStorage.getItem(LS_KEY)||'[]'))}catch(e){return new Set()}}
function save(s){try{localStorage.setItem(LS_KEY,JSON.stringify([...s]))}catch(e){}}
let checked=load();

function total(){return P.reduce((a,p)=>a+p.sections.reduce((b,s)=>b+s.items.length,0),0)}

function upd(){
  const tot=total(),done=checked.size,pct=tot?Math.round(done/tot*100):0;
  document.getElementById('stat').textContent=done+' / '+tot+' ('+pct+'%)';
  document.getElementById('bar').style.width=pct+'%';
}

function render(){
  const c=document.getElementById('cl');c.innerHTML='';
  const ps=curPhase!==null?P.filter(p=>p.id===curPhase):P;
  ps.forEach(ph=>{
    const tot=ph.sections.reduce((a,s)=>a+s.items.length,0);
    const done=ph.sections.reduce((a,s)=>a+s.items.filter((_,i)=>checked.has(ph.id+'_'+s.t+'_'+i)).length,0);
    const wrap=document.createElement('div');
    wrap.className='phase-block';
    const hdr=document.createElement('div');
    hdr.className='phase-hdr';
    hdr.id='ph'+ph.id;
    hdr.style.background=ph.bg;
    hdr.innerHTML=`<h2 style="color:${ph.color}">${ph.label}</h2><span class="phase-count" id="pc${ph.id}" style="color:${ph.color}">${done}/${tot}</span>`;
    wrap.appendChild(hdr);
    ph.sections.forEach(sec=>{
      const vis=curTag?sec.items.filter(i=>i.g===curTag):sec.items;
      if(!vis.length)return;
      const card=document.createElement('div');
      card.className='card';
      const tr=document.createElement('div');
      tr.className='card-title-row';
      tr.innerHTML=`<span class="card-title">${sec.t}</span>${sec.url?`<a class="tpl-link" href="${sec.url}" target="_blank" rel="noopener">↗ Template</a>`:''}`;
      card.appendChild(tr);
      vis.forEach(item=>{
        const oi=sec.items.indexOf(item);
        const k=ph.id+'_'+sec.t+'_'+oi;
        const row=document.createElement('div');
        row.className='item';
        const cb=document.createElement('input');
        cb.type='checkbox';cb.checked=checked.has(k);
        const sp=document.createElement('span');
        sp.className='item-text'+(checked.has(k)?' done':'');
        sp.textContent=item.x;
        const badge=document.createElement('span');
        badge.className='tbadge '+TC[item.g];badge.textContent=TL[item.g];
        cb.addEventListener('change',()=>{
          if(cb.checked)checked.add(k);else checked.delete(k);
          save(checked);
          sp.className='item-text'+(cb.checked?' done':'');
          upd();
          const pTot=ph.sections.reduce((a,s)=>a+s.items.length,0);
          const pDone=ph.sections.reduce((a,s)=>a+s.items.filter((_,i)=>checked.has(ph.id+'_'+s.t+'_'+i)).length,0);
          const pcEl=document.getElementById('pc'+ph.id);
          if(pcEl)pcEl.textContent=pDone+'/'+pTot;
        });
        row.appendChild(cb);row.appendChild(sp);row.appendChild(badge);
        card.appendChild(row);
      });
      wrap.appendChild(card);
    });
    c.appendChild(wrap);
  });
}

function fPhase(p,btn){
  curPhase=p;
  document.querySelectorAll('#phase-btns .fbtn').forEach(b=>b.classList.remove('active'));
  btn.classList.add('active');render();
}
function fTag(t,btn){
  curTag=t;
  document.querySelectorAll('.tag-row .tbtn').forEach(b=>b.classList.remove('active'));
  btn.classList.add('active');render();
}
function resetAll(){
  if(!confirm('Réinitialiser toute la checklist ? Cette action est irréversible.'))return;
  checked.clear();save(checked);render();upd();
}

render();upd();
